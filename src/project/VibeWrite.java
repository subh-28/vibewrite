/**
 * This button saves the happy mood dialog.
 * @param evt 
 */
    private void happySaveButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_happySaveButtonActionPerformed
String name = nameTextField.getText();
String date = happyDateTextField.getText();
String entry = happyTextArea.getText();
String quote = happyQuoteTextField.getText();
       
        
        
        
        // Message in case the textarea is blank.
        if(entry.isEmpty())
        {   
                JOptionPane.showMessageDialog(rootPane,"TextArea cannot be blank!");
            return;
        }
        // Message in case the quote is blank.
        else if(quote.isEmpty())
        {   
                JOptionPane.showMessageDialog(rootPane,"Quote cannot be blank!");
            return;
        }
        // Message in case the date is empty.
        else if(date.isEmpty())
        {   
                JOptionPane.showMessageDialog(rootPane,"Date cannot be blank!");
            return;
        }
        
        // ---------------------------------------       
        //Get a valid name
        // Save the properties file
        //-----------------------------------------
        
        // In case the user doesn't click save then do nothing
        if(fileChooser.showSaveDialog(mainPanel)!= JFileChooser.APPROVE_OPTION)return;
        
        // get the path selected by the user
        File file = fileChooser.getSelectedFile();
        
        try {
            //Create a file
            FileOutputStream output = new FileOutputStream(file);
            
            //Save the data using Properties
            Properties properties = new Properties();
            properties.setProperty("name",name);
            properties.setProperty("date",date);
            properties.setProperty("entry",entry);
            properties.setProperty("quote",quote);
            properties.store(output,"Character data for "+name);
            
            //Let the user know we save the message
            JOptionPane.showMessageDialog(rootPane,"File "+file.getName()+" saved successfully!");
            
            // Reset the GUI
            nameTextField.setText("");
            happyDateTextField.setText("");
            happyTextArea.setText("");
            happyQuoteTextField.setText("");
                    
        } catch (Exception ex) {
            
            //Tell the user that something went wrong
            JOptionPane.showMessageDialog(rootPane, "Could not write "+file.getName());
            //debug
            System.getLogger(VibeWrite.class.getName()).log(System.Logger.Level.ERROR, (String) null, ex);
        }
                     
       
       
   
              
    }//GEN-LAST:event_happySaveButtonActionPerformed
